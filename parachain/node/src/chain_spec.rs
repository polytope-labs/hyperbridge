fn testnet_genesis(
	invulnerables: Vec<(AccountId, AuraId)>,
	endowed_accounts: Vec<AccountId>,
	root: AccountId,
	id: u32,
) -> serde_json::Value {
	let asset_id = MultiLocation::parent();
	let para_id: ParaId = id.into();

	// sibling parachain for tests
	let sibling = match id {
		2000 => ParachainData { id: 2001, slot_duration: 6000 },
		2001 => ParachainData { id: 2000, slot_duration: 6000 },
		_ => ParachainData { id: 1000, slot_duration: 6000 }, /* default to assethub */
	};

	serde_json::json!({
		"balances": {
			"balances": endowed_accounts.iter().cloned().map(|k| (k, 1u64 << 60)).collect::<Vec<_>>(),
		},
		"parachainInfo": {
			"parachainId": para_id,
		},
		"collatorSelection": {
			"invulnerables": invulnerables.iter().cloned().map(|(acc, _)| acc).collect::<Vec<_>>(),
			"candidacyBond": EXISTENTIAL_DEPOSIT * 16,
		},
		"ismpParachain": {
			"parachains": vec![sibling]
		},
		"session": {
			"keys": invulnerables
				.into_iter()
				.map(|(acc, aura)| {
					(
						acc.clone(),                 // account id
						acc,                         // validator id
						session_keys(aura), // session keys
					)
				})
			.collect::<Vec<_>>(),
		},
		"assets": {
			"assets": vec![
				// id, owner, is_sufficient, min_balance
				(asset_id.clone(), root.clone(), true, MICROUNIT),
			],
			"metadata": vec![
				// id, name, symbol, decimals
				(asset_id, "xcDot".as_bytes().to_vec(), "Dot".as_bytes().to_vec(), 10),
			]
		},
		"polkadotXcm": {
			"safeXcmVersion": Some(SAFE_XCM_VERSION),
		},
		"sudo": { "key": Some(root) }
	})
}
